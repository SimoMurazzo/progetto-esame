package com.esame.model;

/**
 * Modello per evento
 * @author Simone Murazzo
 * @author Sergio Galletto
 */

public class Event {

	private String type;
	private String date;
	private String intensity;
	private long ID;
	public String getType() {
		return type;
	}
	
	public String getDate() {
		return date;
	}
	
	public String getIntensity() {
		return intensity;
	}
	
	public long getID() {
		return ID;
	}
	
	
	
}
